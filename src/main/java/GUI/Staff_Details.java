    public void FetchData() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        String query = "FROM Staff";

        try (Session session = HibernateUtils.getSessionFactory().openSession()) {
            model.setRowCount(0);
            Query<Staff> hibernateQuery = session.createQuery(query, Staff.class);
            List<Staff> staffList = hibernateQuery.getResultList();

            for (Staff staff : staffList) {
                String staffId = staff.getStaffId();
                String name = staff.getName();
                String contact = staff.getContact();

                model.addRow(new Object[]{staffId, name, contact});
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

