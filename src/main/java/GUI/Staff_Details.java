    private void keypress(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_keypress
        String bookName = txtsearch.getText().toLowerCase();
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        jTable1.setRowSorter(sorter);
        RowFilter<DefaultTableModel, Object> filter = RowFilter.regexFilter("(?i)" + bookName, 1); 
        sorter.setRowFilter(filter);
        if (bookName.isEmpty()) {
            FetchData();
        }
    }//GEN-LAST:event_keypress

    private void clickTable(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_clickTable
        int row = jTable1.getSelectedRow();
        if (row != -1) {
            txtname.setText(jTable1.getValueAt(row, 1).toString());
            txtcontact.setText(jTable1.getValueAt(row, 2).toString());
            IDs = jTable1.getValueAt(row, 0).toString();
        }

    }//GEN-LAST:event_clickTable

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       String name = txtname.getText();
       String contact = txtcontact.getText();
        try (Session session = HibernateUtils.getSessionFactory().openSession()) {
            session.beginTransaction();

            CriteriaBuilder criteriaBuilder = session.getCriteriaBuilder();
            CriteriaUpdate<Staff> criteriaUpdate = criteriaBuilder.createCriteriaUpdate(Staff.class);
            Root<Staff> root = criteriaUpdate.from(Staff.class);

            criteriaUpdate.set(root.get("name"), name);
            criteriaUpdate.set(root.get("contact"), contact);
            
            criteriaUpdate.where(criteriaBuilder.equal(root.get("staffId"), IDs));
            int rows = session.createQuery(criteriaUpdate).executeUpdate();
            session.getTransaction().commit();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Update successfully");
                FetchData();
            } else {
                JOptionPane.showMessageDialog(this, "No record found");
            }

            txtcontact.setText("");
            txtname.setText("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

